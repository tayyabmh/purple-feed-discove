const nouns = new ethers.Contract(NOUNS_CONTRACT_ADDRESS, CONTRACT_ABI, provider);

// Start of SQL String
let SQL_STRING_CAT_PROFILES = "select p.* from profiles p, verifications v where p.fid = v.fid and v.verified_address in (";
let SQL_STRING_CAT_CASTS = "select c.* from profiles p, casts c, verifications v where p.fid=v.fid and v.verified_address in (";

const nounsOwnersArray = [];

// Loop through all nouns and find their owners
for (let i = 0; i < CURRENT_NOUNS_COUNT; i++) {
  const ownerAddress = await nouns.ownerOf(i);
  console.log("Noun " + i + " is owned by " + ownerAddress);
  nounsOwnersArray.push({
    nounId: i,
    ownerAddress: ownerAddress
  })
}

// Now, let's find unique address instances in the nounsOwnersArray
const uniqueAddresses = [];
nounsOwnersArray.forEach((noun) => {
  if (!uniqueAddresses.includes(noun.ownerAddress)) {
    console.log("Unique address found: " + noun.ownerAddress)
    uniqueAddresses.push(noun.ownerAddress);
  }
})

// Now, let's find the fid's of the unique addresses, if the exist (which they likely should)
const uniqueFids = [];
for (let i = 0; i < uniqueAddresses.length; i++) {
  const fid = await merkleApiClient.lookupUserByVerification(uniqueAddresses[i]);
  if (fid) {
    SQL_STRING_CAT_PROFILES += `'${uniqueAddresses[i].toLowerCase()}' ,`
    SQL_STRING_CAT_CASTS += `'${uniqueAddresses[i].toLowerCase()}' ,`
    uniqueFids.push({
      fid: fid.fid,
      username: fid.username,
      connectedAddress: uniqueAddresses[i]
    });
    console.log(fid);
    console.log("Found fid: " + fid.fid + " for address: " + uniqueAddresses[i])
  }
}

// Button up the SQL String
SQL_STRING_CAT_CASTS = SQL_STRING_CAT_CASTS.slice(0, -1);
SQL_STRING_CAT_PROFILES = SQL_STRING_CAT_PROFILES.slice(0, -1);

SQL_STRING_CAT_PROFILES += `);`
SQL_STRING_CAT_CASTS += `) and p.fid=c.author_fid;`

// Log the SQL String
console.log("PROFILES: ", SQL_STRING_CAT_PROFILES)
console.log("CASTS: ", SQL_STRING_CAT_CASTS)

fs.writeFile(path.join(__dirname, './PurpleHolders'),JSON.stringify(uniqueFids), (err) => {
  if (err) throw err;
  console.log("Success")
})

